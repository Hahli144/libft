#include "libft.h"

int	ft_memcmp(void *s1, void *s2, size_t n)
{
	int i;

	i = 0;
	while ((((unsigned char *)s1)[i] || ((unsigned char*)s2)[i]) && i < (int)n)
	{
		if (((unsigned char*)s1)[i] != ((unsigned char*)s2)[i])
			return (((unsigned char*)s1)[i] - ((unsigned char*)s2)[i]);
		i++;
	}
	return (0);
}
